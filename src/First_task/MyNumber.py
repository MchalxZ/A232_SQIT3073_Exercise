# using '//'

# Integer divided by integer completely producing integer (3)
a= 10 // 3
print("integer divided by integer:", a)

#  Integer divided by float producing float (3.0)
b = 10 // 3.0
print("integer divided by float:", b)

# Float divided by integer producing float (3.0)
c = 10.0 // 3
print("float divided by integer:", c)

# using '/'

# Float divided by float producing float (3.333333....)
d = 10.0 / 3.0
print("float divided by float:", d)

# Integer divided by integer producing float (3.333333.....)
e= 10 / 3
print("integer divided by integer:", e)

# Integer divided by float producing float (3.333333....)
f = 10 / 3.0
print("integer divided by float:", f)
